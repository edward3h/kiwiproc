CREATE TABLE IF NOT EXISTS country (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR NOT NULL,
  code CHAR(3) NOT NULL
);

CREATE TABLE IF NOT EXISTS city (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR NOT NULL,
  country_id INT REFERENCES country(id)
);